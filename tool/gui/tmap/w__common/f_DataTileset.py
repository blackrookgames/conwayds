#region const

TILESET_SIZE = 1024
"""
Number of tiles within a tileset
"""

TILESET_TILE_W = 8
"""
Width of a tile
"""

TILESET_TILE_H = 8
"""
Height of a tile
"""

TILESET_BPP = 4
"""
Bits-per-pixel
"""

#endregion

#region helper fields

_default_data:None|_npt.NDArray[_np.uint8] = None

#endregion

#region helper functions

def _copy_default_data():
    global _default_data
    # Create default data if needed
    if _default_data is None:
        _default_data = _np.zeros(\
            [TILESET_SIZE, TILESET_TILE_W, TILESET_TILE_H],\
            dtype = _np.uint8)
        for _i in range(TILESET_SIZE):
            _digit0 = (_i & 0b1111) | ((_i >> 4) & 0b10000)
            _digit1 = ((_i >> 4) & 0b1111) | ((_i >> 5) & 0b10000)
            # Digit 1
            _bits = _DEF_TILESRC1[_digit1]
            for _y in range(_DEF_H):
                for _x in range(_DEF_W):
                    _default_data[_i, _x, _y] = (_bits & 0b11) * 5
                    _bits >>= 2
            # Digit 0
            _bits = _DEF_TILESRC0[_digit0]
            for _y in range(_DEF_H):
                for _x in range(_DEF_W):
                    _default_data[_i, _DEF_W + _x, _y] = (_bits & 0b11) * 5
                    _bits >>= 2
    # Create copy    
    return _default_data.copy()

#endregion

class DataTileset:
    """
    Represents a tileset
    """

    #region helper const
    
    __MASK = (1 << TILESET_BPP) - 1

    #endregion

    #region init

    def __init__(self, src:None|_img.ImgImagePPixels):
        """
        Initializer for DataTileset

        :param src:
            Source pixels
        """
        self.__data = _copy_default_data()
        # Import tiles
        if src is not None:
            tiles_cols = src.width // TILESET_TILE_W
            tiles_rows = src.height // TILESET_TILE_H
            tiles_total = tiles_cols * tiles_rows
            if tiles_total > 0:
                for _i in range(min(TILESET_SIZE, tiles_total)):
                    _off_x = (_i % tiles_cols) * TILESET_TILE_W
                    _off_y = (_i // tiles_cols) * TILESET_TILE_H
                    # Copy pixels
                    _iy = _off_y
                    for _oy in range(TILESET_TILE_H):
                        _ix = _off_x
                        for _ox in range(TILESET_TILE_W):
                            self.__data[_i, _ox, _oy] = src[_ix, _iy]

    #endregion

    #region operators

    def __getitem__(self, ixy:tuple[int,int,int]):
        """
        Gets the pixel at the specified coordinates

        :param i:
            Tile index
        :param x:
            X-coordinate
        :param y:
            Y-coordinate
        :return:
            Color index of pixel at the specified coordinates
        :raise IndexError:
            One or more coordinates are out of range
        """
        i, x, y = ixy
        try:
            return self.__data[i, x, y]
        except Exception as _e:
            if i < 0 or i >= TILESET_SIZE:
                e = IndexError("Tile index is out of range.")
            elif x < 0 or x >= TILESET_TILE_W:
                e = IndexError("X-coordinate is out of range.")
            elif y < 0 or y >= TILESET_TILE_H:
                e = IndexError("Y-coordinate is out of range.")
            else: e = _e
        raise e

    #endregion